    private void crear_docActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crear_docActionPerformed
        // TODO add your handling code here:
          try {
            File file = new File(DOCS_PATH+"\\"+file_name.getText()+ext_doc.getSelectedItem().toString());
            // Si el archivo no existe es creado
            if (!file.exists()) {
                file.createNewFile();
            }
            else 
            {
                JOptionPane.showMessageDialog(null, "Cannot create file, there is an existing file with this name");
            }
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write("");
            bw.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_crear_docActionPerformed

